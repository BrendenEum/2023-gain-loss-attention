function DisplayFixationCross(expdata)

Screen('FillRect', expdata.windowPtr, expdata.colorBackground);

Screen('DrawLine', expdata.windowPtr, expdata.colorLine, ...
    expdata.screenCenter(1), ...
    expdata.screenCenter(2) - expdata.crossSize/2, ...
    expdata.screenCenter(1), ...
    expdata.screenCenter(2) + expdata.crossSize/2, expdata.lineWidth);
Screen('DrawLine', expdata.windowPtr, expdata.colorLine, ...
    expdata.screenCenter(1) - expdata.crossSize/2, ...
    expdata.screenCenter(2), ...
    expdata.screenCenter(1) + expdata.crossSize/2, ...
    expdata.screenCenter(2), expdata.lineWidth);
Screen('Flip', expdata.windowPtr);

end
